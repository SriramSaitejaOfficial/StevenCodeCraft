//Dynamic typing is the abiility of a programming language to determine the type of a variable at runtime.
//In JavaScript, variables can hold values of any type and can be reassigned to different types at runtime.
let dynamicVariable; //Initially undefined

dynamicVariable = "This is an example of a string";
console.log(dynamicVariable);

//The above is a string, now we're reassigning it to a number.

dynamicVariable = 51;
console.log(dynamicVariable);

//The above is a number, now we're reassigning it to a boolean.

dynamicVariable = true;
console.log(dynamicVariable);
